public class calci{

    public int add (int a, int b)
    {
        int sum= a+b;
        return sum;
    }
    public int add (int a, int b,int c)
    {
        int sum1= a+b+c;
        return sum1;
    }
    public int sub (int a, int b)
    {
        int diff= a-b;
        return diff;
    }
    public int mul (int a, int b)
    {
        int multi = a*b;
        return multi;
    }
    public int div (int a, int b)
    {
        int divi= a/b;
        return divi;
    }
}